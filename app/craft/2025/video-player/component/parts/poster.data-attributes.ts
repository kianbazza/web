export enum PosterDataAttributes {
  /** Applied while the poster image is loading. */
  loading = 'data-loading',
  /** Applied when the poster image has loaded. */
  loaded = 'data-loaded',
  /** Applied on the first frame when entering. Use for CSS enter animations. */
  startingStyle = 'data-starting-style',
  /** Applied during exit animation. Use for CSS exit animations. */
  endingStyle = 'data-ending-style',
}
