export enum VolumeSliderDataAttributes {
  /** Applied when the audio is muted. */
  muted = 'data-muted',
  /** The current volume level (0-1). */
  volume = 'data-volume',
  /** Applied when the slider is being pressed. */
  pressing = 'data-pressing',
  /** Applied when the slider is being dragged after pressing. */
  dragging = 'data-dragging',
}
