export enum VideoDataAttributes {
  /** Applied when the video is playing. */
  playing = 'data-playing',
  /** Applied when the video is paused. */
  paused = 'data-paused',
  /** Applied when the video has ended. */
  ended = 'data-ended',
  /** Applied when the video is buffering/waiting. */
  waiting = 'data-waiting',
  /** Applied when the video is seeking. */
  seeking = 'data-seeking',
}
