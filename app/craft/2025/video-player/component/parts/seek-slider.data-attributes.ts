export enum SeekSliderDataAttributes {
  /** Applied when the video is actively seeking. */
  seeking = 'data-seeking',
  /** Applied when the pointer is hovering over the slider. */
  hovering = 'data-hovering',
  /** Applied when the pointer is pressed down on the slider. */
  pressing = 'data-pressing',
  /** Applied when the user is actively dragging the slider thumb. */
  dragging = 'data-dragging',
}
